def get_population(country):
    left = 0
    right = len(COUNTRIES) - 1
    return binary_search(country, left, right)

def binary_search(country, left, right):
    mid = (right + left) / 2

    if country == COUNTRIES[mid][0]:
        return COUNTRIES[mid][1]
    elif left >= right:
        return None
    else:
        if country < COUNTRIES[mid][0]:
            right = mid - 1
            return binary_search(country, left, right)

        else:
            left = mid + 1
            return binary_search(country, left, right)
